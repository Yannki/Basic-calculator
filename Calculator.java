package sample;

public class Calculator {

    public static double sumNumbers(double a, double b) {
        return a + b;
    }

    public static double subNumbers (double a, double b){
        return a - b;
    }

    public static double multiNumbers (double a, double b){
        return a * b;
    }

    public static double divideNumbers (double a, double b) {
        return a / b;
    }

    public static double percentage (double a) {
        return a / 100;
    }


}
